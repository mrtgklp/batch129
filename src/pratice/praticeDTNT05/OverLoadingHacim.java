public class OverLoadingHacim {
    public int hacimHesapla(int a){
        return a*a*a;//Kup

    }
    public int hacimHesapla(int a,int b){
        return a*a*b;//KarePrizme

    }
    public int hacimHesapla(int a,int b,int c){
        return a*b*c;//dikdörtgen primanın hacmi

    }
}
